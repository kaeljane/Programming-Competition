#include <bits/stdc++.h>
#define ll long long
using namespace std;

// 1
ll binPow(ll a, ll b, ll m) { 
    a %= m;
    ll res = 1;
    while (b > 0) {
        if (b & 1) res = (res * a) % m;
        a = (a*a) % m;
        b >>= 1;
    }
    return res;
}

